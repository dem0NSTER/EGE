res = []

for i in range(0, 10): 
    for j in range(0, 10): 
        if int(f'12345{i}7{j}8') < 10 ** 9: 
            if int(f'12345{i}7{j}8') % 31 == 0: 
                res.append(f'12345{i}7{j}8')

res.sort()
for i in res: 
    print(i, int(i) // 31)
