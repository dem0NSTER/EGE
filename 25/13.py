import fnmatch as fn

s = ''

for i in range(0, 10**10, 1779):
    if fn.fnmatch(str(i), '3?12?14*3'):
        print(i, i // 1779)
        s += str(i) + ' ' 
        s += str(i // 1779) + ' ' 

s = s[:-1]

assert s == '3212014743 1805517 3312314763 1861897 3412614783 1918277 3512114253 1974207 3512914803 1974657 3612414273 2030587 3712714293 2086967'
